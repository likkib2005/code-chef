import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc=new Scanner(System.in);
	    int X=sc.nextInt();
	    if(X>=7)
	    {
	        System.out.println("YES");
	    }
        else if(X>=30){
            System.out.println("NO");
        }
        else{
            System.out.println("NO");
        }

	}
}
